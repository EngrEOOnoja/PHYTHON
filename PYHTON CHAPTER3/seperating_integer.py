miles = int(input())
gallon_used = int(input())
count = 0
while(miles != -1):
	miles = int(input('Enter miles driven (-1 to exit)'))
	gallon_used = int(input('Enter gallon Used'))

	mileage = miles/gallon_used
	print(milage)
	milage += 0
	count += 1
average = milage/count
print("The Total average mileage was", + average)




