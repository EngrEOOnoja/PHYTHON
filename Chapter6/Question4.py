num1 = {1, 2, 4, 8, 16} | {1, 4, 16, 64, 256} # {1,2,4,8,16,64,256}
print(num1)
num2 = {1, 2, 4, 8, 16} & {1, 4, 16, 64, 256} # {1,4,16}
print(num2)
num3 = {1, 2, 4, 8, 16} - {1, 4, 16, 64, 256} # {2,8}
print(num3)
num4 = {1, 2, 4, 8, 16} ^ {1, 4, 16, 64, 256} # {2,8,64,256}
print(num4)
num5 = {1, 2, 4, 8, 16} > {1, 4, 16, 64, 256} # False
print(num5)